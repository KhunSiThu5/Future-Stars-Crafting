package com.model;

public class Video {

	int videoId;
	String videoType;
	String vidLength;
	int vidLike;
	int vidView;
	String ageGroup;
	String vidFileType;
	String vidTitle;

	public int getVideoId() {
		return videoId;
	}

	public void setVideoId(int videoId) {
		this.videoId = videoId;
	}

	public String getVideoType() {
		return videoType;
	}

	public void setVideoType(String videoType) {
		this.videoType = videoType;
	}

	public String getVidLength() {
		return vidLength;
	}

	public void setVidLength(String vidLength) {
		this.vidLength = vidLength;
	}

	public int getVidLike() {
		return vidLike;
	}

	public void setVidLike(int vidLike) {
		this.vidLike = vidLike;
	}

	public int getVidView() {
		return vidView;
	}

	public void setVidView(int vidView) {
		this.vidView = vidView;
	}

	public String getAgeGroup() {
		return ageGroup;
	}

	public void setAgeGroup(String ageGroup) {
		this.ageGroup = ageGroup;
	}

	public String getVidFileType() {
		return vidFileType;
	}

	public void setVidFileType(String vidFileType) {
		this.vidFileType = vidFileType;
	}

	public String getVidTitle() {
		return vidTitle;
	}

	public void setVidTitle(String vidTitle) {
		this.vidTitle = vidTitle;
	}

}
